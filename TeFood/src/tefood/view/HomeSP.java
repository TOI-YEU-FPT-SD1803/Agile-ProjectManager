    private void btnAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddMouseClicked
        // TODO add your handling code here:
        String MaSanPham = txtMaSanPham.getText().trim();
        String TenSanPham = txtTenSanPham.getText().trim();
        Double GiaBan = Double.valueOf(txtGiaBan.getText().trim());
        Integer SoLuongTon = Integer.valueOf(txtSoLuongTon.getText().trim());
        String MoTa = txtMoTa.getText().trim();
        String TrangThai;
        
        if (rdConHang.isSelected()) {
            TrangThai = "Con hang";
        } else {
            TrangThai = "Het hang";
        }
        
        SanPham sp = new SanPham(MaSanPham, TenSanPham, GiaBan, SoLuongTon, MoTa, TrangThai);
        Boolean kq = repositorySP.add(sp);
        
        if (kq == true) {
            JOptionPane.showMessageDialog(this, "Them thanh cong mot san pham!");
            loadData(repositorySP.getList());
        } else {
            JOptionPane.showMessageDialog(this, "Them that bai mot san pham!");
        }
    }//GEN-LAST:event_btnAddMouseClicked

    private void btnUpdateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnUpdateMouseClicked
        // TODO add your handling code here:
        String MaSanPham = txtMaSanPham.getText().trim();
        String TenSanPham = txtTenSanPham.getText().trim();
        Double GiaBan = Double.valueOf(txtGiaBan.getText().trim());
        Integer SoLuongTon = Integer.valueOf(txtSoLuongTon.getText().trim());
        String MoTa = txtMoTa.getText().trim();
        String TrangThai;
        
        if (rdConHang.isSelected()) {
            TrangThai = "Con hang";
        } else {
            TrangThai = "Het hang";
        }
        
        SanPham sp = new SanPham(MaSanPham, TenSanPham, GiaBan, SoLuongTon, MoTa, TrangThai);
        Boolean kq = repositorySP.update(sp);
        
        if (kq == true) {
            JOptionPane.showMessageDialog(this, "Cap nhat thong tin san pham thanh cong!");
            loadData(repositorySP.getList());
        } else {
            JOptionPane.showMessageDialog(this, "Cap nhat thong tin san pham that bai!");
        }
    }//GEN-LAST:event_btnUpdateMouseClicked

    private void btnRemoveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnRemoveMouseClicked
        // TODO add your handling code here:
        String MaSanPham = txtMaSanPham.getText().trim();
        
        Boolean kq = repositorySP.remove(MaSanPham);
        
        if (kq == true) {
            JOptionPane.showMessageDialog(this, "Xoa mot san pham thanh cong!");
            loadData(repositorySP.getList());
        } else {
            JOptionPane.showMessageDialog(this, "Xoa mot san pham that bai!");
        }
    }//GEN-LAST:event_btnRemoveMouseClicked

    private void tblSanPhamMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSanPhamMouseClicked
        // TODO add your handling code here:
        int row = tblSanPham.getSelectedRow();
        
        String Ma = (String) tblSanPham.getValueAt(row, 0);
        SanPham sp = repositorySP.details(Ma);
        
        txtMaSanPham.setText(String.valueOf(sp.getMaSanPham()));
        txtTenSanPham.setText(String.valueOf(sp.getTenSanPham()));
        txtGiaBan.setText(String.valueOf(sp.getGiaBan()));
        txtSoLuongTon.setText(String.valueOf(sp.getSoLuongTon()));
        txtMoTa.setText(String.valueOf(sp.getMoTa()));
        
        if (sp.getTrangThai().equals("Con hang")) {
            rdConHang.setSelected(true);
        } else {
            rdHetHang.setSelected(true);
        }
    }//GEN-LAST:event_tblSanPhamMouseClicked

    private void btnSearchPriceMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSearchPriceMouseClicked
        // TODO add your handling code here:
        Double min = Double.valueOf(txtMin.getText().trim());
        Double max = Double.valueOf(txtMax.getText().trim());
        
        ArrayList<SanPham> spList = repositorySP.searchPrice(min, max);
        loadData(spList);
    }//GEN-LAST:event_btnSearchPriceMouseClicked

