    public void loadData(ArrayList<SanPham> spList) {
        DefaultTableModel tableModel = (DefaultTableModel) tblSanPham.getModel();
        tableModel.setRowCount(0);
        
        for (SanPham sp : spList) {
            tableModel.addRow(new Object[] {
                sp.getMaSanPham(),
                sp.getTenSanPham(),
                sp.getGiaBan(),
                sp.getSoLuongTon(),
                sp.getMoTa(),
                sp.getTrangThai(),
            });
        }
    }

