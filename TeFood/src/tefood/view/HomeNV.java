    void loadData(ArrayList<NhanVien> nvList) {
        DefaultTableModel tableModel = (DefaultTableModel) tblNhanVien.getModel();
        tableModel.setRowCount(0);
        
        for (NhanVien nv : nvList) {
            tableModel.addRow(new Object[] {
                nv.getMaNhanVien(),
                nv.getTenNhanVien(),
                nv.getVaiTro(),
                nv.getSoDienThoai(),
                nv.getEmail(),
                nv.getNgayVaoLam(),
            });
        }
    }

