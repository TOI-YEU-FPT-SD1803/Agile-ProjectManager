    private void tblNhanVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblNhanVienMouseClicked
        // TODO add your handling code here:
        int row = tblNhanVien.getSelectedRow();
        String ma = (String) tblNhanVien.getValueAt(row, 0);
        NhanVien nv = repositoryNV.details(ma);
        
        txtMa.setText(String.valueOf(nv.getMaNhanVien()));
        txtTen.setText(String.valueOf(nv.getTenNhanVien()));
        cboVaiTro.setSelectedItem(nv.getVaiTro());
        txtSDT.setText(String.valueOf(nv.getSoDienThoai()));
        txtEmail.setText(String.valueOf(nv.getEmail()));
        txtNgay.setText(String.valueOf(nv.getNgayVaoLam()));
    }//GEN-LAST:event_tblNhanVienMouseClicked

    private void btnAddMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAddMouseClicked
        // TODO add your handling code here:
        String ma = txtMa.getText().trim();
        String ten = txtTen.getText().trim();
        String vaiTro = cboVaiTro.getSelectedItem().toString();
        String sdt = txtSDT.getText().trim();
        String email = txtEmail.getText().trim();
        Date ngay = Date.valueOf(txtNgay.getText().trim());
        
        NhanVien nv = new NhanVien(ma, ten, vaiTro, sdt, email, ngay);
        
        Boolean kq = repositoryNV.add(nv);
        if (kq == true) {
            JOptionPane.showMessageDialog(this, "Them mot nhan vien moi thanh cong!");
            loadData(repositoryNV.getList());
        } else {
            JOptionPane.showMessageDialog(this, "Them mot nhan vien moi that bai!");
        }
    }//GEN-LAST:event_btnAddMouseClicked

    private void btnUpdateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnUpdateMouseClicked
        // TODO add your handling code here:
        String ma = txtMa.getText().trim();
        String ten = txtTen.getText().trim();
        String vaiTro = cboVaiTro.getSelectedItem().toString();
        String sdt = txtSDT.getText().trim();
        String email = txtEmail.getText().trim();
        Date ngay = Date.valueOf(txtNgay.getText().trim());
        
        NhanVien nv = new NhanVien(ma, ten, vaiTro, sdt, email, ngay);
        
        Boolean kq = repositoryNV.update(nv);
        if (kq == true) {
            JOptionPane.showMessageDialog(this, "Cap nhat thong tin nhan vien thanh cong!");
            loadData(repositoryNV.getList());
        } else {
            JOptionPane.showMessageDialog(this, "Cap nhat thong tin nhan vien that bai!");
        }
    }//GEN-LAST:event_btnUpdateMouseClicked

    private void btnRemoveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnRemoveMouseClicked
        // TODO add your handling code here:
        String ma = txtMa.getText().trim();
        
        Boolean kq = repositoryNV.remove(ma);
        if (kq == true) {
            JOptionPane.showMessageDialog(this, "Xoa mot nhan vien da ton tai thanh cong!");
            loadData(repositoryNV.getList());
        } else {
            JOptionPane.showMessageDialog(this, "Xoa mot nhan vien da ton tai that bai!");
        }
    }//GEN-LAST:event_btnRemoveMouseClicked

    private void btnSearchDateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSearchDateMouseClicked
        // TODO add your handling code here:
        String ngay = String.valueOf(txtNgay.getText().trim());
        
        ArrayList<NhanVien> kq = repositoryNV.searchDate(ngay);
        if (kq.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Khong tim duoc nhan vien nao vao lam ngay: " + ngay);
        } else {
            JOptionPane.showMessageDialog(this, "Tim duoc " + kq.size() +  " nhan vien vao lam ngay: " + ngay);
            loadData(repositoryNV.searchDate(ngay));
        }
    }//GEN-LAST:event_btnSearchDateMouseClicked

